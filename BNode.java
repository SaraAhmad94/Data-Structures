package TreeDS;

public class BNode {
	 int data;
     BNode LeftChild;
     BNode RightChild;
     
     BNode()
     {
    	 data=0;
    	 LeftChild=null;
    	 RightChild=null;
     }
     BNode(int d)
     {   
    	 data=d;
    	 LeftChild=null;
    	 RightChild=null;
     }
    
}
